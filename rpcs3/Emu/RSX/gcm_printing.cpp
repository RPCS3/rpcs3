std::function<std::string(u32)> rsx::get_pretty_printing_function(u32 id)
{
	auto found = printing_functions.find(id);
	if (found != printing_functions.end()) {
		return found->second;
	}

	return [=](u32 v) { return fmt::format("%s : 0x%08x", rsx::get_method_name(id), v); };
}
